char	*ft_strupcase(char *str)
{
	char	ch;

	while (1)
	{
		ch = *str;
		if (ch == 0)
		{
			break ;
		}
		if (ch >= 'a' && ch <= 'z')
		{
			*str = ch - 'a' + 'A';
		}
		str++;
	}
	return (str);
}
