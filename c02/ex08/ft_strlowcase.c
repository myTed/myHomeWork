char	*ft_strlowcase(char *str)
{
	char	ch;

	while (1)
	{
		ch = *str;
		if (ch == 0)
		{
			break ;
		}
		if (ch >= 'A' && ch <= 'Z')
		{
			*str = ch - 'A' + 'a';
		}
		str++;
	}
	return (str);
}
