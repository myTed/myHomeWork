int	ft_str_is_numeric(char *str)
{
	char	ch;

	while (1)
	{
		ch = *str++;
		if (ch == 0)
		{
			break ;
		}
		if (!(ch >= '0' && ch <= '9'))
		{
			return (0);
		}	
	}
	return (1);
}
