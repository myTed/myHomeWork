int	ft_str_is_uppercase(char *str)
{
	char	ch;

	while (1)
	{
		ch = *str++;
		if (ch == 0)
		{
			break ;
		}
		if (!(ch >= 'A' && ch <= 'Z'))
		{
			return (0);
		}
	}
	return (1);
}
